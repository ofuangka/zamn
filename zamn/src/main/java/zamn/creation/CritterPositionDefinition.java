package zamn.creation;

public class CritterPositionDefinition {
	private String id;
	private int seedX;
	private int seedY;

	public String getId() {
		return id;
	}

	public int getSeedX() {
		return seedX;
	}

	public int getSeedY() {
		return seedY;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setSeedX(int seedX) {
		this.seedX = seedX;
	}

	public void setSeedY(int seedY) {
		this.seedY = seedY;
	}
}
